def is_prime(n: int) -> bool:
    """Check if n is prime"""
    if n < 2:
        return False
    for i in range(2, int(n**0.5) + 1):
        if n % i == 0:
            return False
    return True

def prime_index(p: int) -> int:
    """Get the index of prime p (1-indexed)"""
    if not is_prime(p):
        return -1
    count = 0
    for n in range(2, p + 1):
        if is_prime(n):
            count += 1
    return count

